#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - adds two values
 * @a: the first value
 * @b: the second value
 * Return: Returns the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two values
 * @a: the first value
 * @b: the second value
 * Return: Returns the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds two values
 * @a: the first value
 * @b: the second value
 * Return: Returns the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two values
 * @a: the first value
 * @b: the second value
 * Return: Returns the result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds two values
 * @a: the first value
 * @b: the second value
 * Return: Returns the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
